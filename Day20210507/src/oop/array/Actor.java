package oop.array;

public class Actor {

	private String name;
	private int age;
	private String movie;
	
	public Actor(String name, int age, String movie) {
		this.name = name;
		this.age = age;
		this.movie = movie;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getMovie() {
		return movie;
	}
	public void setMovie(String movie) {
		this.movie = movie;
	}
	
	
	
	
}
